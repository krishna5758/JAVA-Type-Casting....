package Narrowing_Casting;

/* NARROWING CASTING > narrowing casting must be done by manually by placing
 *                     the type in parentheses "()" in front of the value. */

public class Demo1 {

	public static void main(String[] args) {
		
		double a = 43;
		
		int b = (int) a;
		
		System.out.println(a);
		System.out.println(b);
	}
}
